<p>Dal Richards  &amp; his orchestra have an enviable reputation for playing the most prestigious gigs around. Whether it's the BC Entertainment Hall of Fame's annual Remembrance Day concert at Vancouver's historic Orpheum theatre or the Arts Club's Annual Fundraising Gala, Tourism Vancouver's Big Band Christmas Party, the Vancouver Art Gallery's 75th Anniversary Gala or the Hotel Vancouver's incomparable Dicken's Christmas Buffet, Dal Richards is the name Vancouverites call on for memorable occasions and memorable music.</p>
<p>Today, young and old enjoy Dal's contemporary sound at venues such as the Chan Centre at UBC, Vancouver's Canada Place, the Massey Theatre in New Westminster, the ACT Theatre in Maple Ridge and the River Rock Resort and Casino in Richmond.</p>
<p>Dal has also combined big band swing with the classics in guest appearances of his band with the Vancouver and Victoria Symphony Orchestras and with the CBC Concert Orchestra. Continuing a tradition, Dal and his orchestra play a seventeen-day run every August at the Pacific National Exhibition.</p>

<p>The Vancouver Sun has described him as <em>'the town favourite&hellip; the dance band king'</em>, while the Province announced in a <em>'Best Entertainment'</em> poll, dance band category that <em>'Dal Richards was a run-away winner, with the next band a distant second'</em>. The Jazz Review in the Ottawa Citizen came on board with <em>'good news for those who enjoy dancing to a smooth jazz oriented big band'</em>.</p>
<p>Today &mdash; Doctor Swing &mdash; as he is affectionately referred to in recognition of his honorary doctorates &mdash; is performing even more gigs than he did when Tommy Dorsey, Harry James and Glenn Miller ruled the nation's jukeboxes and network radio.</p>
<p>Dal is one of the hardest working bandleaders in the business, with his energy and stage presence generating enthusiastic responses and ovations from fans and critics everywhere. Trumpet-playing vocalist Jamie Croil and talented singer - keyboardist Diane Lines complement the vocalizations of the former boy singer and now mature crooner &mdash; Dal Richards &mdash; as he leads some of Vancouver's most experiences musicians, and introduces talented newcomers, in his band.</p>


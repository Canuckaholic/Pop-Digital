    <div class="largeTextTitle">Dal Richards and His Orchestra</div>    
    <p>For more than seven decades, Dal Richards &amp; his Orchestra have delighted Canadian with memorable dance music.</p>
    <p>A Vancouver legend, Dal continues to swing - leading his orchestra of talented musicians in a blend of unique sounds that capture the best of the big band era, along with jazz, rock and pop standards from the 1930s to today. He's played small towns and big cities throughout the province - and continues to bring his unique sound to British Columbians from White Rock to Whistler, New Westminster to Inuvik and beyond.</p>


    <p><strong>Saturday, June 26th</strong><br />
	The Dal Richards Orchestra will kick off a swinging weekend of jazz in Gastown on in Maple Tree Square.  Dal and his 16-piece big band - with featured singers Jennifer Hayes, Diane Lines, Bria Skonberg and Jamie Croil will take to the stage at Carrall and Water Streets from 1 till 2 on Saturday, June 26th.</p>

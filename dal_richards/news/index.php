<h4>February. 13th and 14th, 2008</h4>
<p>Celebrate Valentine's Day with a romantic and exclusive evening of dinner and dancing on the stage of the Chan Shun Concert Hall at the Chan Centre, UBC.</p>
<p>Tasty cuisine plus entertainment by the Dal Richards Orchestra and the UBC Opera Ensemble put the 'fun' in fundraising. Each ticket includes a delicious dinner on the stage of the Chan Centre, vocal bonbons delivered to you by the wonderful Opera Ensemble, and sensational entertainment by one of Vancouver's best-loved bandleaders.</p>
<p>Two nights - Wednesday, Feb 13th &amp; Thurs/Feb 14th - 7-10pm.<br />
  Tickets $150 (with a $100 tax receipt) - but seating is limited so don't delay - call (604) 822-5574 for more information.</p>
<hr />

<h4>August 15, 2007</h4>
<p>Still going stronger than ever, in addition to the release of the <a href="/listen/latestCD.php">new CD</a>, 
Dal at 89, will tap out the tempo, give the downbeat and lead his orchestra in to the opening days at the PNE (Pacific National Exhibition).</p> 
<p>From August 18th to September 3rd the 14-piece Dal Richards Orchestra will be playing two shows a day for all seventeen 
days of the Pacific National Exhibition - appearing daily at noon and two pm on the Rogers Amphitheatre Stage at the Fair; his sixty-eighth consecutive year playing the PNE.</p>

<hr />

<h4>February 6, 2007</h4>
<p>At the Vancouver International Film Center on Tuesday, February 6, 2007, Dal Richards hosts a showing of his all-time favourite film, Casablanca, at the Centre's monthly Cinema Salon at the Vancity Theatre at 1181 Seymour Street at 7:30pm. &ldquo;While almost everyone has seen this war-time classic on video there isn't a lot of opportunity to see it on the big screen &mdash; and it makes a world of difference to see it larger than life&rdquo;, Dal explains. &ldquo;I'm looking forward to sharing some of the stories from behind the scenes about the making of Casablanca&hellip;the intrigues involved in choosing the cast and the music for this award winning classic&hellip;and my all time favourite film.&rdquo;</p>
<p>Tickets are $9.50 and are available in advance at <a href="http://www.vifc.org/" onClick="javascript:urchinTracker('/outgoing/news/vifc.org/');" target="_blank">www.vifc.org</a> or at <strong>604.685.0260</strong><br />
Don't be disappointed, get your tickets soon.</p>


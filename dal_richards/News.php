<h3 class="ContentHeading" style="border-bottom: 1px solid #333333;">December 31, 2006</h3>

<p class="ContentNewsP">At the River Rock Casino, Dal Richards and his Orchestra rang in his 72nd consecutive New Year at a lavish big band dinner dance in the Grand Ballroom of the River Rock Hotel in Richmond. </p>

<p class="ContentNewsP">Dal's first New Year's gig was in 1934-35 as a teen-aged sideman in a band that played the Cinderella Ballroom at Main and 11th. By 1938 Dal was leading his own band with a New Year's Eve gig at the Palomar Supper club, and by 1940 the Dal Richards Orchestra was the house band at the Panorama Roof of the Hotel Vancouver.</p>

<p class="ContentNewsP">New Years' Eve at the Roof was a tradition for Vancouver's elite; and for twenty-five years, the festivities were broadcast nationally on CBC Radio's New Year's Event Cross Canada Dance Party, featuring orchestras from Halifax to Vancouver. </p>

<p class="ContentNewsP">After 72 years and counting, dancing to the swinging big band sounds of Dal Richards and his Orchestra is still considered by many to be the only way to bring in the New Year in Vancouver.</p>


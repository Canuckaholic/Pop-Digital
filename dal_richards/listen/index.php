<p><em>Dal's Place</em> is an hour-long weekly radio show that celebrates the sounds and times of the big band music of the 
1930s, 40s and 50s. Dal Richards' amazing knowledge of the history of the big band era and his warm personal style in
<img src="/Images/DalLeader.jpg" class="pictureFloatLeft" />
telling the stories behind the music have made Dal and <em>Dal's Place</em> a welcome weekend visitor to his listeners homes.</p>

<p><em>Dal's Place</em> has enjoyed an uninterrupted fourteen-year run on four different Vancouver radio stations, and can be heard currently on 650AM on Sunday's at 9pm.</p>

<p>Dal regularly invites local celebrities, performers, musicians and visiting artists to join him on the show for a half-hour of 
talk about their latest projects, the music they enjoy, and their favourite musical stories.</p>

<p>His guests have included such internationally-renown musicians as the Boston Pops' Conductor Keith Lockhart and jazz greats John Pizarelli, Pete Fountain and Doc Severinsen, as well as VSO Music Director Bramwell Tovey;
<img src="/Pictures/BoulevardCasino.jpg" class="pictureFloatRight" />
Vancouver musicians as diverse as blues star Jim Byrnes, chanteuse Dee Daniels, jazz pianist Michael Kaeshammer and vocalist Michael Buble; and local celebrities like Jackson Davies, Vicki 
Gabereau, Christopher Gaze and author Peter C. Newman.</p>

<p><img src="../Images/homepage/AM650_logo.gif" class="pictureFloatLeft" />Dal's relaxed styles and genuine interest in their stories gives <em>Dal's Place</em> listeners a chance to learn to know these people in a unique and 
interesting way. <em>Dal's Place</em> can be heard Sunday nights from 9 till 10 on 650 AM.</p>

<p>For more information, please check out <a href="http://www.am650radio.com/" target="_blank">Dal's Place on 650 AM</a>.</p>

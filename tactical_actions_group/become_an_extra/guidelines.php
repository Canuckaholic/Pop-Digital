    <div class="middleTitle"><a name="8">EXTRAS</a> / BACKGROUND HOLDING:</div>
    <ol>
      <li>When you arrive on set, you will go to extras or background holding. Holding is where all background performers sign in. There will be a list of names of all the extras expected that day. </li>
      <li> Your contact person (or wrangler) signing you in will check off your name and give you a background voucher / time sheet. (See background voucher on how the form is to be completed). Fill the voucher out and hand it back to the person who signed you in. </li>
      <li>Listen carefully to what the wranglers are telling you throughout the day. They will be giving you information about what you will be doing.</li>
      <li>At the end of the day when you are &lsquo;wrapped&rsquo; the voucher will be completed. Ensure it is correct. Your contact person will give you a copy after it is completed. KEEP THE COPY! This is your record that you worked on set and also governs how much you will be paid. If there is a discrepancy or the production misplaces their copy (remember the production accounting office has literally hundreds to deal with), your copy will resolve the issue.&nbsp;<br>
    </li>
    </ol>

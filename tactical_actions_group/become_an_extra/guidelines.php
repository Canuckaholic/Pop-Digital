    <div class="middleTitle"><a name="2">MEASUREMENTS</a>:</div>    
    <p>Your clothing sizes and physical statistics are vital. There are times when you may be asked to wear a uniform or costume. Extras casting directors may even book folks simply on a size. It is very important we have your true measurements. Please do not embellish.&nbsp;</p>

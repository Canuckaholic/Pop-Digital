    <div class="middleTitle"><a name="1">APPEARANCE</a> and PICTURES</div>
    <p>Extras casting directors book background performers on many criteria but primarily appearance. Current pictures of you are a must. Tactical actions Group can take digital pictures of you (at no charge) or you can submit your own. If there are changes to your appearance, provide us with updated photos. Feel free to submit as many different pictures of yourself. Here are some photo guidelines and suggestions:</p>
    <ul>
      <li>No busy backgrounds &ndash; i.e. furniture, trees and plants, wall hangings etc.</li>
      <li>No friends &ndash; just you alone</li>
      <li>Be natural &ndash; don&rsquo;t pose</li>
      <li>Head to Shoulders</li>
      <li>Head to Waist</li>
      <li>Head to Toe</li>
      <li>No Logos on clothing</li>
      <li>Casual clothing</li>
      <li>Sports clothing</li>
      <li>Funky/Trendy clothing</li>
      <li>Vintage clothing</li>
      <li> Gothic clothing</li>
      <li>Suits and Tuxedo</li>
      <li>Ball Gowns and Dresses</li>
      <li>Uniforms</li>
      <li>Muscle Shirt (if fit and muscular)</li>
      <li>Things that you do &ndash; horseback riding, military, police, any sport, motorcycle, etc.</li>
    </ul>

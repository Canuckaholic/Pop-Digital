	<div class="middleTitle"><a name="3"></a>AVAILABILITY &amp; COMMITMENT:</div>    
    <ol>
      <li>How available you are to work is one of the factors that dictates how much work you receive. Please, on a weekly basis, let us know your availability simply by emailing us which days you can work. </li>
      <li>Production shoot days are often 12-16 hours long. When you indicate your availability and accept a booking, keep in mind you are committing to as much time as you may be required. Ensure you are free of other commitments and obligations prior to accepting a booking. </li>
      <li>We may also call you to confirm your availability for a booking. If we are unable to reach you, we will leave a message. It is imperative you check you messages frequently and call us back ASAP.</li>
      <li>Once you have accepted a booking you are expected to fulfill your commitment.&nbsp;<br>
      </li>
    </ol>
	<p>| <a href="#index">INDEX</a> |</p>
	<hr />
    <div class="middleTitle"><a name="4">CALL</a> TIME:</div>    
      <ol>
        <li>Your call time is the time that you are required to be on set. </li>
        <li>We will contact you before 11:00pm the night before the day you are to be on set. If you are to be on a late afternoon or night shoot, you may receive your call time in the morning of the shoot.</li>
        <li>We will also provide you with other pertinent information such as location and contact person.</li>
        <li>If we are unable to reach you, we will leave a message. It is imperative you check your messages frequently and confirm with us that you have received the information.</li>
      </ol>
	<p>| <a href="#index">INDEX</a> |</p>
	<hr />
    <div class="middleTitle"><a name="5">CLOTHING</a> / WARDROBE:</div>    
    <ol>
      <li>You will be informed of the type of clothing you are required to wear and bring to set. </li>
      <li>Take with you 3-5 changes neat and pressed in a garment bag. </li>
      <li> Do not bring bright neon colours nor clothing with logos unless asked to do so. </li>
      <li>Bring accessories that compliment what you will be wearing. </li>
      <li>If you are to wear a uniform provided by the production, arrangements will be made prior to your shoot day. </li>
      <li>Wardrobe personnel on set will look over your clothing and make any final changes if needed.&nbsp;<br>
      </li>
    </ol>
	<p>| <a href="#index">INDEX</a> |</p>
	<hr />
    <div class="middleTitle"><a name="6">GROOMING</a> / HAIR &amp; MAKE-UP</div>    
    <ol>
      <li>Always arrive on set well groomed. Show up looking as you would want to have your picture taken. </li>
      <li>Men are to be clean-shaven unless otherwise instructed. </li>
      <li>Women should arrive with hair and make up done. It is recommended women also bring their personal cosmetics bag. </li>
      <li>Hair &amp; Make-up personnel on set will look you over and make any final changes. Once you have been checked over, do not make any changes.&nbsp;
    </li>
    </ol>
	<p>| <a href="#index">INDEX</a> |</p>
	<hr />
    <div class="middleTitle"><a name="7">BE</a> EARLY:</div>
    <p>Please be 15-20 minutes early prior to your call time.</p>
    <p>Where you sign in on set may be located some distance from where shooting is occurring and you may need the time to get there. You may also need the time to get dressed if you are to be wearing specific wardrobe. There are also unforeseen circumstances (traffic, ahead of shooting schedule, etc) in which the extra time becomes very useful. </p>
    <p>If for any reason you are going to be late, immediately call T.A.G. (778-889-6824).</p>

    <div class="middleTitle"><a name="12">PAYMENT</a> / FEES:</div>
    <ol>
      <li>Tactical Actions Group is an agency that works off of commission. We do not get paid until you get paid. Our only fee is our commission. There are no registration fees, no photo fees, and no administration fees.    </li>
      <li>Your earnings are processed by the accounting department of the production company and is calculated from the background voucher you signed on set. Once this is completed, a cheque for your services will be sent to Tactical Actions Group. This process could take up to 2 &ndash; 3 weeks. </li>
      <li>Always keep all copies of your background vouchers. These vouchers are your record of the productions you have worked on as well as the dates and times. </li>
      <li>When Tactical Actions Group receives your cheque from the production company, we will notify you that your cheque is ready for pickup from our office. We will also advise you of the commission owed to Tactical Actions Group. </li>
      <li>Commission is 15% based on your gross pay. GST is also payable but is based on the commission, not your gross pay. </li>
      <li>At present, since you are considered to be under contract, Income Tax, CPP and E.I. are not deducted by either Tactical Actions Group or the production companies. The filing of your taxes to the CRA is your responsibility. Tactical Actions Group will not remit taxes on your behalf. </li>
      <li>You must have your commission payment with you when you pick up your cheque. You can pay your commission by either cash or cheque. We do not accept credit nor debit cards. </li>
      <li> If your commission cheque returns NSF, you will be responsible for both the commission payment and any NSF associated bank charges. You are expected to rectify the issue immediately.</li>
    </ol>
